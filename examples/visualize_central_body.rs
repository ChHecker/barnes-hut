use barnes_hut::{
    interaction::gravity::GravitationalAcceleration, particle_creator::CentralBodyParticleCreator,
    visualization::Visualizer,
};
use rand_distr::{Normal, Uniform};

const SPEED: f32 = 1.;

fn main() {
    // Generate random particles.
    let uniform_mass = Uniform::new(0., 1e4);
    let normal_rad = Normal::new(2., 0.2).unwrap();

    let acc = GravitationalAcceleration::new(1e-6);
    let pc = CentralBodyParticleCreator::new(1e10, uniform_mass, normal_rad);

    // Visualize.
    let vis = Visualizer::from_particle_creator(pc, 2, acc, 1920, 1080).unwrap();
    vis.visualize(SPEED, 1.5).unwrap();
}
